int		ft_is_prime(int nb)
{
	int a;
	int b;

	a = 0;
	b = 0;
	if (nb < 0 || nb == 0 || nb == 1)
	{
		return (0);
	}
	while (b * a < nb && a < (nb / 2))
	{
		a++;
		while (a * b < nb && b < (nb / 2))
		{
			b++;
		}
	}
	if (a * b == nb)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
