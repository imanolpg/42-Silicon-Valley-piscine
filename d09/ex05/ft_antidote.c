int		ft_antidote(int i, int j, int k)
{
	if ((i > j) && (k > i))
	{
		return (i);
	}
	if ((j > k) && (i > j))
	{
		return (j);
	}
	if ((k > i) && (j > k))
	{
		return (k);
	}
}
