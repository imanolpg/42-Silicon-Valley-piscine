int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (tab[n] != '\0')
	{
		if ((*f)(tab[n]) == 1)
			i++;
		n++;
	}
	return (i);
}
